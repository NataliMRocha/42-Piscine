#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	if (n >= 0)
	{
		return (ft_putchar ('P'));
	}
	else
	{
		return (ft_putchar ('N'));
	}
}
